// Simple function example

function greet() {
  console.log("Hello! Welcome to JavaScript functions.");
}

// Calling (executing) the function
greet();



// Parametric Function

// Function with parameters
function addNumbers(a, b) {
  let sum = a + b;
  console.log("Sum:", sum);
}

// Call (use) the function with arguments
addNumbers(5, 3);
addNumbers(10, 20);
